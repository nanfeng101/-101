package com.example.demo.entity;

public class Song_list {
    private int id;
    private String title;
    private String pic;
    private int username_id;
    private String username;
    private String introduction;
    private String style;
    private String song_name;
    private String song_introduction;
    private String url;
    private String song_pic;
    private String singer_name;
    private int play_sum;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public int getUsername_id() {
        return username_id;
    }

    public void setUsername_id(int username_id) {
        this.username_id = username_id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getSong_name() {
        return song_name;
    }

    public void setSong_name(String song_name) {
        this.song_name = song_name;
    }

    public String getSong_introduction() {
        return song_introduction;
    }

    public void setSong_introduction(String song_introduction) {
        this.song_introduction = song_introduction;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getSong_pic() {
        return song_pic;
    }

    public void setSong_pic(String song_pic) {
        this.song_pic = song_pic;
    }

    public String getSinger_name() {
        return singer_name;
    }

    public void setSinger_name(String singer_name) {
        this.singer_name = singer_name;
    }

    public int getPlay_sum() {
        return play_sum;
    }

    public void setPlay_sum(int play_sum) {
        this.play_sum = play_sum;
    }
}
